const int N = 1e5 + 10;
int T, n, m, ans, p, l, r, k, cnt;
struct node {
    int l, r, sum;
}Tree[N * 40];
int root[N];

struct num {
    int x, id;
    bool operator < (const num &rhs) const {
        return x < rhs.x;
    }
}a[N];

int rnk[N];

void init() {
    cnt = 0;
    root[0] = 0;
    Tree[0].l = Tree[0].r = Tree[0].sum = 0;
}

void update(int &rt, int l, int r, int num) {
    Tree[++cnt] = Tree[rt];
    rt = cnt;
    Tree[rt].sum++;
    if(l == r)
        return;
    int mid = l + r >> 1;
    if(num <= mid)
        update(Tree[rt].l, l, mid, num);
    else
        update(Tree[rt].r, mid + 1, r, num);
}

int query(int i, int j, int k, int l, int r) {
    if(l == r)
        return l;
    int mid = l + r >> 1;
    int d = Tree[Tree[j].l].sum - Tree[Tree[i].l].sum;
    if(k <= d)
        return query(Tree[i].l, Tree[j].l, k, l, mid);
    else
        return query(Tree[i].r, Tree[j].r, k - d, mid + 1, r);
}

inline int check(int fir, int l, int r, int k) {
    return max(abs(p - a[query(root[l - 1], root[r], fir, 1, n)].x), abs(p - a[query(root[l - 1], root[r], fir + k - 1, 1, n)].x));
}

int main() {
    read(T);
    //scanf("%d", &T);
    while(T--) {
        read(n); read(m);
        //scanf("%d%d", &n, &m);
        ans = 0; init();
        for(int i = 1; i <= n; ++i) {
            read(a[i].x);
            //scanf("%d", &a[i].x);
            a[i].id = i;
        }
        sort(a + 1, a + n + 1);
        for(int i = 1; i <= n; ++i)
            rnk[a[i].id] = i;
        for(int i = 1; i <= n; ++i) {
            root[i] = root[i - 1];
            update(root[i], 1, n, rnk[i]);
        }
        while(m--) {
            //scanf("%d%d%d%d", &l, &r, &p, &k);
            read(l); read(r); read(p); read(k);
            l ^= ans; r ^= ans; p ^= ans; k ^= ans;
            int ll = 1, rr = r - l + 1 - k + 1, tt = 0x3f3f3f3f;
            while(ll <= rr) {
                int lll = ll + (rr - ll) / 3, rrr = rr - (rr - ll) / 3, mid = ll + rr >> 1;
                if(ll < mid && mid < rr) lll = mid, rrr = mid + 1;
                // cout << ll << " "  << rr << " " << lll << " " << rrr << " " << tt << endl;
                int pl = check(lll, l, r, k), pr = check(rrr, l, r, k);
                if(pl <= pr) {
                    tt = min(pl, tt);
                    rr = rrr - 1;
                }
                else {
                    tt = min(pr, tt);
                    ll = lll + 1;
                }
            }
            println(ans = tt);
        }
    }
    return 0;
}